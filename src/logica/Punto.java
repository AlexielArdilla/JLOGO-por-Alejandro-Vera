package logica;



public class Punto {
	
	private int x;//aqui se debe posicionar la tortuga
	private int y;//esto se debe mostrar
	
	public Punto(int x, int y){
		
		this.x = x;
		
		this.y = y;
			
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
		
	}
	
}
